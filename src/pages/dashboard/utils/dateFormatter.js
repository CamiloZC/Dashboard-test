export const formatMonth = (month) => {
    if(month === '1/' || month === '1'){
        return 'Jan'
    }
    if(month === '2/' || month === '2'){
        return 'Feb'
    }
    if(month === '3/' || month === '3'){
        return 'Mar'
    }
    if(month === '4/' || month === '4'){
        return 'Apr'
    }
    if(month === '5/' || month === '5'){
        return 'May'
    }
    if(month === '6/' || month === '6'){
        return 'Jun'
    }
    if(month === '7/' || month === '7'){
        return 'Jul'
    }
    if(month === '8/' || month === '8'){
        return 'Aug'
    }
    if(month === '9/' || month === '9'){
        return 'Sep'
    }
    if(month === '10/' || month === '10'){
        return 'Oct'
    }
    if(month === '11/' || month === '11'){
        return 'Nov'
    }
    if(month === '12/' || month === '12'){
        return 'Dec'
    }
}